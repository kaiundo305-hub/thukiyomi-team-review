// 今日の日付から現在のフェーズを返す
function getCurrentPhaseInfo() {
  var todayStr = new Date().toISOString().split('T')[0];
  var allPhases = [];
  MOON_CYCLE_DATA.cycles.forEach(function(cycle) {
    PHASE_ORDER.forEach(function(phase) {
      allPhases.push({ cycleId: cycle.id, phase: phase, date: cycle[phase].date });
    });
  });
  for (var i = 0; i < allPhases.length; i++) {
    var cur = allPhases[i], nxt = allPhases[i + 1];
    if (!nxt || (todayStr >= cur.date && todayStr < nxt.date)) {
      var cycle = MOON_CYCLE_DATA.cycles.find(function(c) { return c.id === cur.cycleId; });
      return { cycleId: cur.cycleId, phase: cur.phase, cycle: cycle, phaseData: cycle[cur.phase] };
    }
  }
  return { cycleId: allPhases[0].cycleId, phase: allPhases[0].phase,
           cycle: MOON_CYCLE_DATA.cycles[0], phaseData: MOON_CYCLE_DATA.cycles[0][allPhases[0].phase] };
}

